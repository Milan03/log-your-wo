function createPdfData(
    log: SimpleLog,
    labels: WorkoutPdfLabels = englishLabels
): WorkoutPdfData {
    return {
        log,
        weightMeasure: 'lbs',
        distanceMeasure: 'km',
        elapsedTimeLabel: '1h 5m',
        locale: 'en-CA',
        labels
    };
}

function createExercise(name: string, completed: boolean): Exercise {
    const exercise = new Exercise();
    exercise.exerciseType = 'strength';
    exercise.exerciseName = name;
    exercise.completed = completed;
    return exercise;
}
